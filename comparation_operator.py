#comporation operator adalah operasi perbandigan untuuk menentukan nilai logika artau kondisi(biasanya..) ini output nya cuma true false
x = 10
y = 16

#untuk (=) gabisa pakai itu karena itu berarti  memasukan nilai ke variable kayak contoh diatas, kita pakai (==)
print(x == y)
#tidak sama dengan kita pakai(!=)
print(x != y)
#lebih besar dari kita pakai(>)
print(x > y)
#lebuuih kecil dari kita pakai(<)
print(x < y)
#lebih kecil atau sama dengan (<=)
print(x <= y)
#lebih besar atau sama dengan (>=)
print(x >= y)

